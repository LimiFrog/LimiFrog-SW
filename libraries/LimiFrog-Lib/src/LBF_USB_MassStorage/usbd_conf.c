/**
  * @brief  Initializes the PCD MSP.
  * @param  hpcd: PCD handle
  * @retval None
  */
void HAL_PCD_MspInit(PCD_HandleTypeDef *hpcd)
{

// NOTE:
//  USB pin inits (DP, DM, VBUS_SENSE=) done at start-up 
// in LBF_Fixed_Inits() calling USB_IOcfg()

  
  /* Enable power to USB peripheral (now separate domain) */
  HAL_PWREx_EnableVddUSB();


  /* Enable USB Clock */
   __HAL_RCC_USB_OTG_FS_CLK_ENABLE();  //changed for L4 vs L1

  
  /* Enable SYSCFG Clock: already done in Inits elsewhere */


  /* Set USB Interrupt priority */
#ifdef __OTG_FS_IRQn_PRIO
  // Update by LimiFrog : (__OTG_FS_IRQn_PRIO defined in LBF_Interrupts_Cfg.h)
  HAL_NVIC_SetPriority(OTG_FS_IRQn, __OTG_FS_IRQn_PRIO, 0);
#else
  // Original by ST:  
  HAL_NVIC_SetPriority(OTG_FS_IRQn, 7, 0);
#endif


  /* Enable USB Interrupt */
  HAL_NVIC_EnableIRQ(OTG_FS_IRQn);

}


/**
  * @brief  De-Initializes the PCD MSP.
  * @param  hpcd: PCD handle
  * @retval None
  */
void HAL_PCD_MspDeInit(PCD_HandleTypeDef *hpcd)
{
  /* Disable USB FS Clock */
  __HAL_RCC_USB_OTG_FS_CLK_DISABLE();
  
  /* Disable SYSCFG Clock */
  __HAL_RCC_SYSCFG_CLK_DISABLE(); 
}

